SELECT 
		--COUNT(*)
		A.D1_COD
--UPDATE A SET A.D1_CUSTO=B.D2_CUSTO1
FROM SD1010 A (NOLOCK)
LEFT OUTER JOIN  SD2010 B (NOLOCK) ON B.D_E_L_E_T_='' AND B.D2_FILIAL=A.D1_LOJA AND B.D2_DOC=A.D1_DOC AND B.D2_SERIE=A.D1_SERIE AND B.D2_COD=A.D1_COD AND D2_QUANT=D1_QUANT 
AND B.D2_CLIENTE='000800'  
WHERE	A.D_E_L_E_T_=''
		AND A.D1_TIPO='N'
		AND A.D1_FORNECE='000010'
		AND A.D1_DTDIGIT BETWEEN '20200601' AND '20200630'
		AND A.D1_CUSTO!=B.D2_CUSTO1
ORDER BY D1_COD
