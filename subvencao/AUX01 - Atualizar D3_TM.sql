/**
    QUERY PARA ATUALIZAR O D3_TM
*/


SELECT 
        SD3.D3_FILIAL,
        SD3.D3_EMISSAO,
        SD3.D3_COD,
        SD3.D3_LOCAL,
        SD3.D3_QUANT,
        XSD3.D3_QUANT
 --UPDATE SD3 SET SD3.D3_TM = '700', SD3.D3_CUSTO1=XSD3.D3_CUSTO1
FROM    SD3010 SD3 (NOLOCK)
LEFT JOIN SD3010 XSD3 (NOLOCK) ON 
    XSD3.D_E_L_E_T_='' 
    AND XSD3.D3_FILIAL=SD3.D3_FILIAL 
    AND XSD3.D3_COD=SD3.D3_COD 
    AND XSD3.D3_LOCAL=SD3.D3_LOCAL
    AND XSD3.D3_QUANT=SD3.D3_QUANT
    AND XSD3.D3_TM = '200'
    AND XSD3.D3_EMISSAO BETWEEN '20200601' AND '20200630'
WHERE   SD3.D_E_L_E_T_ = ''
        AND SD3.D3_TM = '600'
        AND SD3.D3_EMISSAO BETWEEN '20200701' AND '20200731'